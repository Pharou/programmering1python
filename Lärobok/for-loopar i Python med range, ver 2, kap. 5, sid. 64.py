# For-loop med en range - nedräkning av tal
# Här visar jag hur du kan räkna med från ett startvärde till ett stoppvärde 
# som ej inkluderas i loopen, utelämnas startvärde räknar man från 0
# ett tredje tal anger här att man ska stega bakåt i talföljden i och med att 
# det är negativt.
 
for i in range(20, 1, -3):
    print(i)  