#!/usr/bin/python3

# Filnamn: while-loopar, exempel 2, kap. 5, sid. 65.py

# while-loop som räknare
# Loopen skriver ut ordet Hej fem gånger
# Därefter skrivs texten "Hemskt mycket hej!"

i = 1
while i <= 5:
    print('Hej!')  
    i += 1

# Satsen nedan körs så fort loopens villkor inte uppfylls (blir falskt)
print('Hemskt mycket hej!')