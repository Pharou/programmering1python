# if - else

y = 4

# med två if-satser
if y > 5:
    print('y är större än fem')
if y <= 5:
    print('y är mindre eller lika med fem')
    
# samma sak med if - else
if y > 5:
    print('y är större än fem')
else:
    print('y är mindre eller lika med fem')


